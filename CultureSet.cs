using System;
using System.Collections.Generic;
using System.Globalization;
using System.Linq;
using System.Text;

namespace Angel_Access
{
    public class ForceCulture : IDisposable
    {
        private readonly CultureInfo _oldCulture;

        public ForceCulture(CultureInfo newCulture)
        {
            _oldCulture = CultureInfo.CurrentCulture;
            CultureInfo.CurrentCulture = newCulture ?? CultureInfo.InvariantCulture;
            GC.SuppressFinalize(this);
        }

        public void Dispose()
        {
            Thread.CurrentThread.CurrentCulture = _oldCulture;
        }
    }
}
